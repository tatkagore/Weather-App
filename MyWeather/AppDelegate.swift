        let apiKey = Bundle.main.object(forInfoDictionaryKey: "GOOGLE_API_KEY") as? String
        
        guard let key = apiKey, !key.isEmpty else {
            print("Google API key does not exist")
            return false
        }

        GMSPlacesClient.provideAPIKey(key)
        
        return true
    }

